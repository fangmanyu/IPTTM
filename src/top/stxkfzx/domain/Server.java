package top.stxkfzx.domain;
/**
 * 服务器信息表对应javabean
 * @author fmy
 *
 */
public class Server {

	private String sername;
	private double serall;
	private int seryear;
	private int sermonth;
	public String getSername() {
		return sername;
	}
	public void setSername(String sername) {
		this.sername = sername;
	}
	public double getSerall() {
		return serall;
	}
	public void setSerall(double serall) {
		this.serall = serall;
	}
	public int getSeryear() {
		return seryear;
	}
	public void setSeryear(int seryear) {
		this.seryear = seryear;
	}
	public int getSermonth() {
		return sermonth;
	}
	public void setSermonth(int sermonth) {
		this.sermonth = sermonth;
	}
	
	
}
